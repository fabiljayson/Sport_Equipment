    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String search = c_search.getText();
        try{
            java.sql.Statement s = db.mycon().createStatement();
            ResultSet  rs = s.executeQuery(" SELECT * FROM clients WHERE cid = '"+search+"'");
            if(rs.next()){
                c_nom.setText(rs.getString("Client_name"));
                c_tp.setText(rs.getString("Tp_numero"));
                c_adr.setText(rs.getString("address"));
                c_typ.setText(rs.getString("type"));
                c_cper.setText(rs.getString("contract"));
                c_tel.setText(rs.getString("tel"));
            }
        }catch(SQLException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

